package com.practicejava;

public class DefaultPrimitiveValues {
        static byte b;
        static short s;
        static int i;
        static long l;
        static float f;
        static double d;
        static char c;
        static boolean bl;
        static String str;

    public static void main(String[] args) {
        System.out.println("The default value of Byte datatype is "+b);
        System.out.println("The default value of Short datatype is "+s);
        System.out.println("The default value of Int datatype is "+i);
        System.out.println("The default value of long datatype is "+l);
        System.out.println("The default value of float datatype is "+f);
        System.out.println("The default value of double datatype is "+d);
        System.out.println("The default value of char datatype is "+c);
        System.out.println("The default value of boolean datatype is "+bl);
        System.out.println("The default value of String datatype is "+str);

    }
}
